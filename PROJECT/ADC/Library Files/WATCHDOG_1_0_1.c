/**************************************************************************************************
 *	FUNCTIONs
 *************************************************************************************************/
/*
 * 	Function:	wdtSetup
 *	Purpose	: 	Setup Watchdog timer
 * 	Input	: 	WDT_O: turn on or off WDT?
 * 				WDT_TIME_: Interval of time-out
 * 	Output	: 	None
 */
void wdtSetup (unsigned int WDT_O, unsigned int WDT_TIME_)
{
	switch (WDT_O)
	{
		case WDT_OFF:
			WDTCTL = WDTPW + WDTHOLD;
			break;
		case WDT_ON:
			WDTCTL = WDTPW + WDTCNTCL + WDT_TIME_;
			break;
	}
}
//-------------------------------------------------------------------------------------------------
/*
 * 	Function: 	wdtClear
 *	Purpose	: 	Clear Watchdog counter to prevent a PUC
 * 	Input	: 	None
 * 	Output	: 	None
 */
void wdtClear (void)
{
	WDTCTL = WDTPW + WDTCNTCL;
}
